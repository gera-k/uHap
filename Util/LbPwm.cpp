void LbPWM::rgb(uint8_t& R, uint8_t& G, uint8_t& B)
{
    if (!_on)
    {
        R = G = B = 0;
        return;
    }

    float V = (float)_v / 100.;
    float S = (float)_s / 100.;
    float H = (float)_h;
    
    float C = V * S;
    float X = C * (1. - fabs(fmod(H/60, 2) - 1));
    float m = V - C;
    float r, g, b;
    if (H < 60)
    {
        r = C; g = X, b = 0;
    }
    else if (H < 120)
    {
        r = X; g = C, b = 0;
    }
    else if (H < 180)
    {
        r = 0; g = C, b = X;
    }
    else if (H < 240)
    {
        r = 0; g = X, b = C;
    }
    else if (H < 300)
    {
        r = X; g = 0, b = C;
    }
    else
    {
        r = C; g = 0, b = X;
    }

    R = (uint8_t)((r + m) * 255);
    G = (uint8_t)((g + m) * 255);
    B = (uint8_t)((b + m) * 255);

    //Log::Msg("h s v %d %d %d  RGB %02X %02X %02X\n", _h, _s, _v, R, G, B);
}

